// Deterministic generator for the 10 A/B testing codes.
// Codes are guaranteed not to collide with the 1002 production codes.
// Output: supabase/migrations/20260428200000_lottery_test_codes.sql
//         scripts/lottery-test-codes.md

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');

const SEED = 20260429;

// Each row defines the predetermined outcome of one of the 10 test codes.
// `prize === null` means non-winner. `kind` is metadata for the QA team.
const ASSIGNMENTS = [
  { kind: 'fixed',  prize: 50,   note: 'Fixed 50₪ winner' },
  { kind: 'fixed',  prize: 100,  note: 'Fixed 100₪ winner' },
  { kind: 'fixed',  prize: 200,  note: 'Fixed 200₪ winner' },
  { kind: 'fixed',  prize: 500,  note: 'Fixed 500₪ winner' },
  { kind: 'fixed',  prize: 1000, note: 'Fixed 1000₪ winner' },
  { kind: 'fixed',  prize: null, note: 'Fixed non-winner' },
  { kind: 'random', prize: 100,  note: 'Random predetermined 100₪' },
  { kind: 'random', prize: 200,  note: 'Random predetermined 200₪' },
  { kind: 'random', prize: null, note: 'Random predetermined non-winner' },
  { kind: 'random', prize: null, note: 'Random predetermined non-winner' },
];

function mulberry32(seed) {
  return function () {
    seed |= 0;
    seed = (seed + 0x6d2b79f5) | 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

const ALPHABET = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';

function generateCode(rng) {
  let code = '';
  for (let i = 0; i < 6; i++) {
    code += ALPHABET[Math.floor(rng() * ALPHABET.length)];
  }
  return code;
}

const productionCodes = new Set(
  JSON.parse(readFileSync(resolve(repoRoot, 'scripts/lottery-codes.json'), 'utf8')),
);

const rng = mulberry32(SEED);
const seen = new Set();
const codes = [];
while (codes.length < ASSIGNMENTS.length) {
  const c = generateCode(rng);
  if (productionCodes.has(c) || seen.has(c)) continue;
  seen.add(c);
  codes.push(c);
}

const allocations = ASSIGNMENTS.map((a, i) => ({ ...a, code: codes[i], index: i + 1 }));

const today = new Date().toISOString();

const sql = [
  '-- ============================================================',
  '-- A/B testing coupon codes (pre-launch QA)',
  `-- Generated by scripts/generate-test-codes.mjs (seed=${SEED}) at ${today}`,
  '-- 10 test codes that override the main lottery logic for QA',
  '-- ============================================================',
  '',
  'CREATE TABLE IF NOT EXISTS lottery_test_codes (',
  '  code         text PRIMARY KEY,',
  '  prize_amount integer,',
  '  is_active    boolean NOT NULL DEFAULT true,',
  '  notes        text,',
  '  created_at   timestamptz NOT NULL DEFAULT now()',
  ');',
  '',
  'ALTER TABLE lottery_test_codes ENABLE ROW LEVEL SECURITY;',
  '-- No policies = deny all direct access; only the SECURITY DEFINER RPC can read this.',
  '',
  '-- Seed the 10 test codes (idempotent — re-running keeps existing rows fresh)',
  'INSERT INTO lottery_test_codes (code, prize_amount, notes) VALUES',
];
sql.push(
  ...allocations.map((a, i) => {
    const prize = a.prize === null ? 'NULL' : a.prize;
    const note = a.note.replace(/'/g, "''");
    const sep = i === allocations.length - 1 ? '' : ',';
    return `  ('${a.code}', ${prize}, '#${a.index} — ${note}')${sep}`;
  }),
);
sql.push(
  'ON CONFLICT (code) DO UPDATE SET',
  '  prize_amount = EXCLUDED.prize_amount,',
  '  notes        = EXCLUDED.notes,',
  '  is_active    = true;',
  '',
  '-- Replace the redemption RPC: test codes are checked BEFORE production codes',
  'DROP FUNCTION IF EXISTS redeem_lottery_code(text, text, text, text, text);',
  '',
  'CREATE OR REPLACE FUNCTION redeem_lottery_code(',
  '  p_code       text,',
  '  p_first_name text,',
  '  p_last_name  text,',
  '  p_phone      text,',
  '  p_venue      text',
  ')',
  'RETURNS jsonb',
  'LANGUAGE plpgsql',
  'SECURITY DEFINER',
  'SET search_path = public',
  'AS $$',
  'DECLARE',
  '  v_code_row    lottery_codes%ROWTYPE;',
  '  v_test_row    lottery_test_codes%ROWTYPE;',
  '  v_entry_id    uuid;',
  '  v_clean_code  text;',
  '  v_clean_phone text;',
  '  v_clean_venue text;',
  'BEGIN',
  '  v_clean_code  := upper(trim(p_code));',
  '  v_clean_phone := trim(p_phone);',
  '  v_clean_venue := trim(p_venue);',
  '',
  '  -- TEST CODE OVERRIDE: bypass all production checks for QA',
  '  SELECT * INTO v_test_row',
  '  FROM lottery_test_codes',
  '  WHERE code = v_clean_code AND is_active = true;',
  '',
  '  IF FOUND THEN',
  '    RETURN jsonb_build_object(',
  "      'success', true,",
  "      'entry_id', NULL,",
  "      'prize_amount', v_test_row.prize_amount,",
  "      'is_test', true",
  '    );',
  '  END IF;',
  '',
  '  -- Production logic (unchanged from prior migration)',
  '  IF length(v_clean_code) != 6 THEN',
  "    RETURN jsonb_build_object('success', false, 'error', 'INVALID_CODE_FORMAT');",
  '  END IF;',
  '',
  "  IF trim(p_first_name) = '' OR trim(p_last_name) = '' OR v_clean_phone = '' OR v_clean_venue = '' THEN",
  "    RETURN jsonb_build_object('success', false, 'error', 'MISSING_FIELDS');",
  '  END IF;',
  '',
  "  IF (SELECT count(*) FROM lottery_entries WHERE submitted_at > now() - interval '1 minute') >= 10 THEN",
  "    RETURN jsonb_build_object('success', false, 'error', 'RATE_LIMITED');",
  '  END IF;',
  '',
  '  SELECT * INTO v_code_row',
  '  FROM lottery_codes',
  '  WHERE code = v_clean_code',
  '  FOR UPDATE;',
  '',
  '  IF NOT FOUND THEN',
  "    RETURN jsonb_build_object('success', false, 'error', 'CODE_NOT_FOUND');",
  '  END IF;',
  '',
  '  IF v_code_row.is_used THEN',
  "    RETURN jsonb_build_object('success', false, 'error', 'CODE_ALREADY_USED');",
  '  END IF;',
  '',
  '  IF EXISTS (SELECT 1 FROM lottery_entries WHERE phone = v_clean_phone) THEN',
  "    RETURN jsonb_build_object('success', false, 'error', 'DUPLICATE_PHONE');",
  '  END IF;',
  '',
  '  UPDATE lottery_codes',
  '  SET is_used = true, used_at = now()',
  '  WHERE id = v_code_row.id;',
  '',
  '  INSERT INTO lottery_entries (code_id, first_name, last_name, phone, venue)',
  '  VALUES (v_code_row.id, trim(p_first_name), trim(p_last_name), v_clean_phone, v_clean_venue)',
  '  RETURNING id INTO v_entry_id;',
  '',
  '  RETURN jsonb_build_object(',
  "    'success', true,",
  "    'entry_id', v_entry_id,",
  "    'prize_amount', v_code_row.prize_amount",
  '  );',
  'END;',
  '$$;',
  '',
  'GRANT EXECUTE ON FUNCTION redeem_lottery_code(text, text, text, text, text) TO anon;',
  'GRANT EXECUTE ON FUNCTION redeem_lottery_code(text, text, text, text, text) TO authenticated;',
);

writeFileSync(
  resolve(repoRoot, 'supabase/migrations/20260428200000_lottery_test_codes.sql'),
  sql.join('\n') + '\n',
);

const md = [
  '# A/B testing coupon codes',
  '',
  `**Seed:** \`${SEED}\``,
  `**Generated:** ${today}`,
  '**Total:** 10 test codes (override production logic via RPC)',
  '',
  '| # | Code | Outcome | Type |',
  '|---|------|---------|------|',
];
for (const a of allocations) {
  const outcome = a.prize === null ? 'Non-winner' : `${a.prize}₪`;
  const type = a.kind === 'fixed' ? 'Fixed' : 'Random (undisclosed to QA)';
  md.push(`| ${a.index} | \`${a.code}\` | ${outcome} | ${type} |`);
}
md.push('');
md.push('## Disable / remove');
md.push('');
md.push('- Disable one code: `UPDATE lottery_test_codes SET is_active = false WHERE code = \'XXXXXX\';`');
md.push('- Disable all (instant kill switch): `UPDATE lottery_test_codes SET is_active = false;`');
md.push('- Permanent removal at launch: drop the table + apply a follow-up migration that reverts the RPC to production-only logic.');
writeFileSync(resolve(repoRoot, 'scripts/lottery-test-codes.md'), md.join('\n') + '\n');

